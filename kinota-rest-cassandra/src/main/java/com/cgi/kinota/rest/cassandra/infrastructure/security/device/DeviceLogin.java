package com.cgi.kinota.rest.cassandra.infrastructure.security.device;

import com.fasterxml.jackson.annotation.JsonCreator;
import com.fasterxml.jackson.annotation.JsonProperty;

import java.io.Serializable;

/**
 * Created by dfladung on 3/27/17.
 */
public class DeviceLogin implements Serializable {

    String id;
    String key;

    @JsonCreator
    public DeviceLogin(@JsonProperty("id") String id, @JsonProperty("key") String key) {
        this.id = id;
        this.key = key;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }
}
